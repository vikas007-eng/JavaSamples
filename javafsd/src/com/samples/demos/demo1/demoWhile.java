package com.samples.demos.demo1;

public class demoWhile {
  public static void main(String[] args) {
	
	  int count = 5;
	  while(count != 0) {
		  System.out.println(count);
		  count--;
	  }
	  
	  int count2 = -1;
	  do {
		  System.out.println(count2);
		  count2--;
	  }while(count>0);
	  
	 
}
}
